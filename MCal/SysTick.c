void SysTick_Init(void)
{
	/*enable interrupt 
	 * clock source Precision internal oscillator (PIOSC) divided by 4
	 * enable counter
	 */
	  STCTRL = 0x00000007;
	/*value = 128*/
  	STRELOAD = 0x00000080;
	/*one second delay relaod value at 16 MHz
	STRELOAD = 0x00F423FF;*/
	 /*Call back pointer at timer overflow*/
    SysTick_OVF_CBK_Ptr=SysTick_Cbk_ptr;
	   
}
 
